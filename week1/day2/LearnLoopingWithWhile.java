package week1.day2;

public class LearnLoopingWithWhile {

	public static void main(String[] args) {
		
		// In selenium automation, we will not be using while loop, we will be often using for loop only.

		int n = 5;
		
		while(n<10) {
			System.out.println(n);
			n++;
		}
	}

}

// When should we use for loop or while loop?
// If you know the number of iteration, use for loop
// If you don't know the number of iteration, use while loop
// In do while, the code will be getting executed atleast once

